import threading
from bs4 import BeautifulSoup
import requests
from selenium import webdriver
from selenium.webdriver.chrome.options import Options
from selenium.webdriver.chrome.service import Service
from webdriver_manager.chrome import ChromeDriverManager
import pandas as pd
import time

def scrape_single_url(url):
    try:
        if "nairaland.com" in url:
            # Use requests for static pages with a User-Agent header
            headers = {
                "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/91.0.4472.124 Safari/537.36"
            }
            response = requests.get(url, headers=headers, timeout=10)
            response.raise_for_status()  # Raise an error for bad responses (4xx or 5xx)
            soup = BeautifulSoup(response.content, "html.parser")
            
            # Extract content
            title = soup.title.string if soup.title else "No Title"
            content = soup.get_text(separator="\n")[:1000]  # Limit content size
            
            return {
                "URL": url,
                "Title": title,
                "Content": content,
                "Timestamp": pd.Timestamp.now()
            }
        else:
            # Use Selenium for dynamic pages
            chrome_options = Options()
            chrome_options.add_argument("--headless")  # Run in headless mode
            chrome_options.add_argument("--disable-gpu")
            chrome_options.add_argument("--no-sandbox")
            chrome_options.add_argument("user-agent=Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/91.0.4472.124 Safari/537.36")
            
            driver = webdriver.Chrome(service=Service(ChromeDriverManager().install()), options=chrome_options)
            driver.get(url)
            time.sleep(5)  # Wait for page to load
            soup = BeautifulSoup(driver.page_source, "html.parser")
            driver.quit()
            
            # Extract content
            title = soup.title.string if soup.title else "No Title"
            content = soup.get_text(separator="\n")[:1000]  # Limit content size
            
            return {
                "URL": url,
                "Title": title,
                "Content": content,
                "Timestamp": pd.Timestamp.now()
            }
    except Exception as e:
        print(f"Error scraping {url}: {str(e)}")
        return None

def scrape_urls(urls, progress_bar, status_text):
    scraped_data = []
    total_urls = len(urls)
    
    def worker(url, index):
        result = scrape_single_url(url)
        if result:
            scraped_data.append(result)
        
        # Update progress bar
        progress_bar.progress((index + 1) / total_urls)
        status_text.text(f"Scraping {index + 1}/{total_urls} URLs...")
    
    threads = []
    for i, url in enumerate(urls):
        thread = threading.Thread(target=worker, args=(url, i))
        threads.append(thread)
        thread.start()
    
    for thread in threads:
        thread.join()
    
    return scraped_data
